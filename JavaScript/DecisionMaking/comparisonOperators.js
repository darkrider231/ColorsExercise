// COMPARISON OPERATORS

// Greater Than (Example: >)
console.log(3 > 1); // true
console.log(0.9 > 1); // false

// Less Than (Example: <)
console.log(3 < 1); // false
console.log(0.9 < 1); // true

// Greater Than or Equal to (Example: >=)
console.log(3 >= 3); // true
console.log(3 >= 2); // true

// Less Than or Equal to (Example: <=)
console.log(3 <= 3); // true
console.log(3 <= 2); // false

// Equal Comparisons
// Double Equals (Example: ==)
console.log(3 == 3); // true
console.log(3 == 2); // false
console.log(3 == "3"); // true

// Triple Equals (AKA Strict Equality) (Example: ===)
// Checks if both values are the same data type
console.log(3 === 3); // true
console.log(3 === "3"); // false

// Not Equal Comparisons (AKA Single not equal) (Example: !=)
// Checks to see if values are not equaal to each other
console.log(3 != 3); // false
console.log(3 != 2); // true

// Strict Not Equal (Example: !==)
// Checks to see if values are not equal to each other
console.log(3 !== 3); // false
console.log(7 !== "7"); // true

